	#DEFINE	FIM_PERIODO_FLAG    FIM_PERIODO,0   ;FIM_PERIODO = 0 - NÃO ACABOU / 1 - ACABOU
	#DEFINE AJUSTE_THETA_FLAG   AJUSTE_POSICAO,0;AJUSTE_THETA = 0 - AINDA NÃO AJUSTADO / 1 -  JÁ AJUSTADO
	#DEFINE AJUSTE_PHI_FLAG	    AJUSTE_POSICAO,1;AJUSTE_PHI = 0 - AINDA NÃO AJUSTADO / 1 - JÁ AJUSTADO
	#DEFINE FIM_CICLO_FLAG	    AJUSTE_POSICAO,2;FIM_CICLO = 0 - AINDA EM FASE DE POSICIONAMENTO / 1 - MOTORES POSICIONADOS
	#DEFINE	MODO_INICIALIZACAO  AJUSTE_POSICAO,3;MODO = 0 - INICIALIZACAO / 1 - EM FUNCIONAMENTO
	#DEFINE	ANGULO_ATIVO_FLAG   ANGULO_ATIVO,0  ;ANGULO_ATIVO = 0 - THETA(MOTOR1) / 1 - PHI(MOTOR2)
	
	
